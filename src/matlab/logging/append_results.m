function append_results(log_file, results)
    %APPEND_RESULTS Append an array of result structs to CSV.

    for i = 1:numel(results)
        fid = fopen(log_file, 'a');
        assert(fid ~= -1, 'Cannot open %s for appending.', log_file);
        fprintf(fid, '%d,%.4f,%s,%d,%.0f,%.2f,%.4f,%.4f,%.4f\n', ...
            i, ...
            results(i).AUC, ...
            results(i).TimeElapsed, ...
            results(i).K, ...
            results(i).TrainRatio * 100, ...
            results(i).Threshold, ...
            results(i).Precision, ...
            results(i).Recall, ...
            results(i).F1Score);
        fclose(fid);
    end
end
